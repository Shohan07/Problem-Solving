#include<bits/stdc++.h>
using namespace std;
int main()
{
    long long int t, n, i;
    cin >> t;
    while(t--){
        cin >> n;
        if(n == 1){
            cout << -1 << endl;
            continue;
        }
        cout << 2;
        for(i = 1; i < n; i++){
            cout << 7;
        }
        cout << endl;
    }
    return 0;
}
